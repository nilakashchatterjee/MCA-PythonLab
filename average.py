#In this program we are going to aggregate the marks of two subject and print it
s1 = float(input("Enter the marks of 1st subject: ")) #mark of subject 1
s2 = float(input("Enter the marks of 2nd subject: ")) #mark of subject 2

av = (s1 + s2)/2 #aggregate operation of two subjects

#printing the aggregate as output
print("The average marks of given two subject is:",av)